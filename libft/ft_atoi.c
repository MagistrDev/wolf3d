int		ft_atoi(const char *str)
{
	int		flag;
	int		nb;
	char	*s;

	nb = 0;
	flag = 1;
	s = (char*)str;
	while (*s == 9 || *s == 10 || *s == 11 || *s == 12 || *s == 13 || *s == 32)
		s++;
	if (*s == '-')
		flag = -1;
	if (*s == '-' || *s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		nb = nb * 10 + *s - 48;
		s++;
	}
	if ((s - (char*)str) > 25 && flag == 1)
		return (-1);
	if ((s - (char*)str) > 19 && flag == -1)
		return (0);
	return (flag * nb);
}
