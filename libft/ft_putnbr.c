#include <unistd.h>

void	ft_putnbr(int n)
{
	int		i;
	char	rev[12];

	(n < 0) ? write(1, "-", 1) : (void)i;
	if (n == -2147483648)
	{
		write(1, "2147483648", 10);
		return ;
	}
	else if (n == 0)
	{
		write(1, "0", 1);
		return ;
	}
	else if (n < 0)
		n *= -1;
	i = 0;
	while (n > 0)
	{
		rev[i++] = (n % 10 + 48);
		n /= 10;
	}
	while (i-- > 0)
		write(1, &rev[i], 1);
}
